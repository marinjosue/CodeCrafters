    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
    String user = txtUser.getText();
    String password = txtPassword.getText();

    try {
        Document query = new Document("user", user);
        Document existingUser = collection.find(query).first();

        if (existingUser != null) {
            String stored = existingUser.getString("password"); 

            AESCipher aesCipher = new AESCipher();
            String storedDecrypted = aesCipher.descifra(stored); 

            if (storedDecrypted.equals(password)) { 
                UserData.userCi = user;
                MenuOwner owner = new MenuOwner();
                owner.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Contrase√±a incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al procesar la solicitud", "Error", JOptionPane.ERROR_MESSAGE);
    }  

        
    }//GEN-LAST:event_btnIniciarActionPerformed

